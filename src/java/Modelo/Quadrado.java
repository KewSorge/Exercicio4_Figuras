public class Quadrado {
    private int lado;
    private double area;

    public Quadrado() {
    }

    public Quadrado(int lado) {
        this.lado = lado;
    }

    public int getLado() {
        return lado;
    }

    public void setLado(int lado) {
        this.lado = lado;
    }

    public double getArea() {
        return area;
    }

    public void setArea(double area) {
        this.area = area;
    }
      public void calArea(){
        area = lado*lado;
    }
}
